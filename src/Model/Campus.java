public class Campus {
    private String nome;
    private Integer id;
    
    public Campus(){
        
    }
    public Campus(Integer id){
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Integer getId() {    
        return id;
    }
}
